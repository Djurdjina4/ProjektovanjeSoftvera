    private void btnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajActionPerformed
        String url = txtURL.getText();
        String user = txtUsername.getText();
        String password = String.valueOf(txtPassword.getPassword());
        String port = txtBrojPorta.getText();

        FileOutputStream out = null;
        try {
            out = new FileOutputStream("db.config.properties");
            FileInputStream in = new FileInputStream("db.config.properties");
            Properties props = new Properties();
            props.load(in);
            in.close();

            props.setProperty("url", url);
            props.setProperty("username", user);
            props.setProperty("password", password);
            props.setProperty("port", port);
            props.store(out, null);
            out.close();
            new ServerskaForma().setVisible(true);
        } catch (FileNotFoundException ex) {
            Logger.getLogger(FrmKonfiguracijaBaze.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(FrmKonfiguracijaBaze.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            try {
                out.close();
            } catch (IOException ex) {
                Logger.getLogger(FrmKonfiguracijaBaze.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        JOptionPane.showMessageDialog(this, "Parametri baze su promenjeni");
        this.dispose();

    }//GEN-LAST:event_btnSacuvajActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        String password = new String(txtPassword.getPassword());
        if (txtUsername.getText().isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Popunite polja!");
        }
        if (showPassword.getText().equals("")) {
            txtPassword.setEchoChar((char) 0);
            showPassword.setText("Set");
        } else {
            txtPassword.setEchoChar('*');
            showPassword.setText("");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

