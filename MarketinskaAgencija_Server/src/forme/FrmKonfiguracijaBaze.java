    private void ucitajIzFajlaPodatke() {
        DBProperties properties;
        try {
            properties = new DBProperties();

            txtURL.setText(properties.vratiDBURL());
            txtUsername.setText(properties.vratiDBKorisnik());
            txtBrojPorta.setText(properties.vratiDBPort());
           // txtPassword.setText(properties.vratiDBSifra());
            String pass=properties.vratiDBSifra();
            String sifra="";
            for(int i=0; i<pass.length(); i++){
                sifra+="*";
            }
            txtPassword.setText(sifra);
            
        } catch (IOException ex) {
            Logger.getLogger(FrmKonfiguracijaBaze.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
