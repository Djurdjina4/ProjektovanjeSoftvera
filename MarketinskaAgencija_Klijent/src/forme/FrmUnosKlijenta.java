    private boolean validate(String kompanija, String osoba, String grad, String email, String telefon) throws InterruptedException {
        String regex = "[a-zA-z .]+";
        String numbers = "^\\+\\d{8,15}$";
        String emailForm = "^(.+)@(.+)$";

        //OBAVEZNA POLJA
        if (kompanija.isEmpty()) {
            lblKompanijaError.setText("Obavezno polje!");
            txtKompanija.setBorder(new LineBorder(Color.red, 2));
        }
        if (osoba.isEmpty()) {
            lblOsobaError.setText("Obavezno polje!");
            txtOsoba.setBorder(new LineBorder(Color.red, 2));
        }
        if (grad.isEmpty()) {
            lblGradError.setText("Obavezno polje!");
            txtGrad.setBorder(new LineBorder(Color.red, 2));
        }
        if (email.isEmpty()) {
            lblEmailError.setText("Obavezno polje!");
            txtEmail.setBorder(new LineBorder(Color.red, 2));
        }
        if (telefon.isEmpty()) {
            lblBrojTelefonaError.setText("Obavezno polje!");
            txtBrojTelefona.setBorder(new LineBorder(Color.red, 2));
            return false;
        }

        //KOMAPANIJA
        if (!kompanija.matches(regex)) {
            JOptionPane.showMessageDialog(this, "Polje kompanija nije popunjeno na pravilan naƒçin!");
            return false;
        }

        //OSOBA
        if (!osoba.matches(regex)) {
            JOptionPane.showMessageDialog(this, "Polje osoba nije popunjeno na pravilan naƒçin!");
            return false;
        }

        //GRAD
        if (!grad.matches(regex)) {
            JOptionPane.showMessageDialog(this, "Polje grad nije popunjeno na pravilan naƒçin!");
            return false;
        }

        //EMAIL - format: xx@xx
        if (!email.matches(emailForm)) {
            JOptionPane.showMessageDialog(this, "Polje email nije popunjeno na pravilan naƒçin!");
            return false;
        }

        //TELEFON
        //duzina telefona
        if (telefon.length() < 9 || telefon.length() > 16) {
            JOptionPane.showMessageDialog(this, "Broj telefona mora sadr≈æati 8-15 cifara!");
            return false;
        }

        //plus
        if (telefon.charAt(0) != '+') {
            JOptionPane.showMessageDialog(this, "Broj telefona mora poƒçinjati znakom +!");
            return false;
        }

        if (!telefon.matches(numbers)) {
            JOptionPane.showMessageDialog(this, "Polje broj telefona nije popunjeno na pravilan naƒçin!");
            return false;
        }

        return true;
    }

